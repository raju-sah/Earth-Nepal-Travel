<script type="text/javascript">
  $(document).ready(function() {
    $('#subscribe_form').on('submit', function(e) {
      e.preventDefault();

      var form = this; // Capture reference to the form element

      if ($('#po_ps').val()) {
        Swal.fire({
          icon: 'error',
          title: 'Fake Content detected!',
        }).then((result) => {
          form.reset();
        });
      } else {
        $.ajax({
          type: 'POST',
          url: "{{route('front.newsletter')}}",
          data: $(form).serialize(),
          headers: {
            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
          },
          success: function(response) {
            swalMessage('success', response.message);
            form.reset();
          },
          error: function(xhr) {
            if (xhr.status === 422) {
              displayErrors(xhr.responseJSON.errors);
            } else {
              swalMessage('error', xhr.responseText);
            }
          }
        });
      }
    });

    function swalMessage(type, message) {
      Swal.fire({
        toast: true,
        position: 'top-end',
        timer: 1500,
        timerProgressBar: true,
        icon: type,
        title: message,
        showConfirmButton: false,
      });
    }
  });
</script>

